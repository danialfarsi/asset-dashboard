    @classmethod
    def calculate_semantic_similarity(cls, text1, text2):
        words1 = set(re.sub(r'[^\w\s]', '', text1).split())
        words2 = set(re.sub(r'[^\w\s]', '', text2).split())
        if not words1 or not words2:
            return 0
        
        expanded1 = set()
        expanded2 = set()
        
        for word in words1:
            expanded1.add(word)
            for key, synonyms in cls.SYNONYMS.items():
                if word in synonyms or word == key:
                    expanded1.add(key)
                    expanded1.update(synonyms)
        
        for word in words2:
            expanded2.add(word)
            for key, synonyms in cls.SYNONYMS.items():
                if word in synonyms or word == key:
                    expanded2.add(key)
                    expanded2.update(synonyms)
        
        intersection = len(expanded1.intersection(expanded2))
        union = len(expanded1.union(expanded2))
        if union == 0:
            return 0
        return intersection / union
    
    @classmethod
    def find_best_match(cls, asset_name):
        all_asset_types = AssetType.objects.all()
        if not all_asset_types:
            return None
        
        scores = []
        for at in all_asset_types:
            semantic = cls.calculate_semantic_similarity(asset_name, at.name)
            textual = SequenceMatcher(None, asset_name, at.name).ratio()
            final = (semantic * 0.7) + (textual * 0.3)
            
            scores.append({
                'asset_type': at,
                'score': final,
                'semantic_score': semantic,
                'textual_score': textual,
                'name': at.name
            })
        
        scores.sort(key=lambda x: x['score'], reverse=True)
        if scores and scores[0]['score'] > 0.1:
            return scores[0]
        return None
    
    @classmethod
    def get_best_asset_type(cls, asset_name):
        result = cls.find_best_match(asset_name)
        if result:
            return result['asset_type']
        return None
    
    @classmethod
    def get_best_valuation_method(cls, asset_name):
        result = cls.find_best_match(asset_name)
        if result:
            template = ScreeningTemplate.objects.filter(asset_type=result['asset_type']).first()
            if template:
                return template.valuation_method
        return 'M-05'
    
    @classmethod
    def explain_match(cls, asset_name):
        result = cls.find_best_match(asset_name)
        if not result:
            return None
        return {
            'asset_type': result['asset_type'].name,
            'asset_type_id': result['asset_type'].id,
            'score': result['score'],
            'confidence': f'{min(result["score"] * 2, 1.0) * 100:.1f}%',
            'details': {
                'معنایی': f'{result["semantic_score"] * 100:.1f}%',
                'متنی': f'{result["textual_score"] * 100:.1f}%',
            }
        }


